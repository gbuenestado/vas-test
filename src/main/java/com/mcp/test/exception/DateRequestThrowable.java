package com.mcp.test.exception;

public class DateRequestThrowable extends Throwable {

	private static final long serialVersionUID = 6071077580484256925L;

	public DateRequestThrowable() {
		super();
	}

	public DateRequestThrowable(String message, Throwable cause) {
		super(message, cause);
	}

	public DateRequestThrowable(String message) {
		super(message);
	}

	public DateRequestThrowable(Throwable cause) {
		super(cause);
	}

}
